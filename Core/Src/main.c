void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
	HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &RxHeader, RxData);
	//CANMessageRx = 1;
	raw_spe = RxData[0];

	if(raw_spe > 0){
		zero_counter = 0;
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_SET); //R1 = 1
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_SET); //R2 = 1
		//0 = 0%
		//25 = 100%
		duty_cycle = raw_spe*0.04*65535;
		TIM16->CCR1 = duty_cycle;
	} else if(raw_spe == 0){
		zero_counter++;
		if(zero_counter >= 50){
			TIM16->CCR1 = 0;
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_RESET); //R1 = 0;
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET); //R2 = 0;
		}
	}
}

